package com.product.manager.entity.request;

public class RequestQueryProductFamily {
		
	private String codigoProducto;
	
	private String nombreProducto;
	
	private String activoProducto;

	public String getCodigoProducto() {
		return codigoProducto;
	}

	public void setCodigoProducto(String codigoProducto) {
		this.codigoProducto = codigoProducto;
	}

	public String getNombreProducto() {
		return nombreProducto;
	}

	public void setNombreProducto(String nombreProducto) {
		this.nombreProducto = nombreProducto;
	}

	public String getActivoProducto() {
		return activoProducto;
	}

	public void setActivoProducto(String activoProducto) {
		this.activoProducto = activoProducto;
	}

}
